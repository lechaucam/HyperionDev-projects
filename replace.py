# the next sentence is copied from assignment and named as the variable 'sentence'
sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog."

# replacing all '!' into a ' ' and print
sentence1 = (sentence.replace("!"," "))
print(sentence1)

# convert sentence1 into capital letters and print
sentence_capital= sentence1.upper()
print(sentence_capital)

# reverse sentence1
print(sentence1[::-1])

# reverse sentence_capital
print(sentence_capital[::-1])